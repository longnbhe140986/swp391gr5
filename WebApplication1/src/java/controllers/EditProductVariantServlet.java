    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String variantId = request.getParameter("variant_id");
        Product variant = productDAO.getVariantById(Integer.parseInt(variantId));
        request.setAttribute("variant", variant);
        request.getRequestDispatcher("EditProductVariant.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        int variantId = Integer.parseInt(request.getParameter("variant_id"));
        String color = request.getParameter("color");
        String size = request.getParameter("size");
        int stock = Integer.parseInt(request.getParameter("stock"));
        
        Product variant = new Product(variantId, color, size, stock);
        productDAO.updateProduct(variant);
        response.sendRedirect("products");
    }

